# Области видимости - определяет границы дейсвия переменных
# Локальные перменные не доступны глобально
# Глобальные перменные доступны локально (например внутри функции)

a = 10  #Глобальная переменная

def my_fn():
	a = True  # Локальная перменная
	b = 15  # Локальная перменная
	print(a)
	print(b)

my_fn()

print(a)
print(b)

