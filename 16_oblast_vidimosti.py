# Жизненный цикл переменных

a = 10  # 1 - Глобальное объявление


def my_fn():
	a = True  # 3 Локальное объявление
	b = 15  
	print(a) # 4 Объявлена ли a в области видимости функции? (да доступно и в терминал вывод)
	print(b)


my_fn()  # 2 Вызов функции

print(a) # 5 Вывод значения из глобальной области 


# Внутри функций можно создавать глобальные перменные через global

def my_fn():
	global a  
	a = 15

my_fn()
print(a)

