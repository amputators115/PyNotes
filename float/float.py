# Числа с десятичной точкой

a = 45.78
print(type(a))

# Конвертация к целому числу

temperatura = 36.6
new_temp = int(temperatura)
print(new_temp)
print(type(new_temp))

temperatura = int(36.6)
print(temperatura)
print(type(temperatura))

# Конвертация строки к float

temperatura = float('36.6')
print(temperatura)
print(type(temperatura))

# Округление до целого числа round()

temperatura = round(36.6)
print(temperatura)
print(type(temperatura))

rate = 0.78
print(round(rate))
print(type(rate))
