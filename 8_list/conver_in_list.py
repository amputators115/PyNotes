# Конвертация в список - втсроенная функция list()

# Конвертация строки

my_str = 'Hello Word!'
print(my_str)
print(type(my_str))
print(len(my_str))
print(id(my_str))

new_str_list = list(my_str)
print(new_str_list)
print(type(new_str_list))
print(len(new_str_list))
print(id(new_str_list))

