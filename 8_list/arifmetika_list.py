# Арифметические операции в списках

ratings = [4.6, 7.8, 3, 8.9, 2.1]
print(min(ratings))
print(max(ratings))
print(sum(ratings))
print(sum(ratings)/len(ratings))
print(ratings[0] + ratings[3])
print(ratings[0] / ratings[3])
print(ratings[0] * ratings[3])
print(ratings[0] ** ratings[3])
print(ratings[0] % ratings[3])
print(ratings[4] // ratings[1])

